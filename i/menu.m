% --- Executes on button press in pushbutton1.
function pushbutton1_Callback(hObject, eventdata, handles)



files = dir('*.jpg');
[n,m] = size(files);
i = 1;
while (i < n +1)
    m = 1;
    n_funciones = 4;
    while (m < n_funciones + 1)
        if (m == 1)
            r = amaro(files(i).name);
            if (r == 1)
                m =  n_funciones + 3;
                rr = 'Amaro';
                %set(handles.text2,'String',i);
            end
        end
        if (m == 2)
            r = rise(files(i).name);
            if (r == 1)
                m =  n_funciones + 3;
                rr = 'Rise';
                %set(handles.text2,'String',i);
            end
        end
        if (m == 3)
            r = ink(files(i).name);            
            if (r == 1)
                m =  n_funciones + 3;                
                rr = 'InkJet';
                %set(handles.text2,'String',i);
            end
        end
        if (m == 4)
            r = willo(files(i).name);
            if (r == 1)
                m =  n_funciones + 3;
                rr = 'Willow';
                %set(handles.text2,'String',i);
            end
            
        end
        m = m +1;
        if (m < n_funciones + 1)
            rr = 'Normal';
        end
    end
    resultados(i,:) = {files(i).name,rr};
    i = i + 1;
end

tabla = uitable;
set(tabla,'Data',resultados,'ColumnName',{'Nombre','Filtro'});

